import UIKit

extension UIFont {
    
    static func signInButtonsFont() -> UIFont? {
        return UIFont(name: "PlusJakartaSans-Regular", size: 12)
    }
    
    static func trialButtonFont() -> UIFont? {
        return UIFont(name: "PlusJakartaSans-Regular", size: 16)
    }
    
    static func premiumButtonFont() -> UIFont? {
        return UIFont(name: "PlusJakartaSans-Bold", size: 16)
    }
}
